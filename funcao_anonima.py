# 11. **Função Anônima**:
# - Crie uma função anônima e atribua-a a uma variável chamada `multiplicar`. 
# A função deve receber dois números como argumentos e retornar a multiplicação deles.



multiplicar = lambda num1, num2 : num1 * num2    # função anônima multiplicar com dois argumentos num1 e num2


print(multiplicar(10, 9))                  # chama  a função multiplicar com os argumentos 
 
 